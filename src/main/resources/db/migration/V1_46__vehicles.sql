create table vehicle_base (
    id bigserial,
    name varchar(32) not null,
    max_level int not null,
    engine_quality varchar(20) not null,
    frame_quality varchar(20) not null,
    computer_quality varchar(20) not null,
    special_part1quality varchar(20),
    special_part2quality varchar(20),
    special_part3quality varchar(20),
    primary key (id)
);

create table vehicle (
    id bigserial,
    player_id bigint not null,
    base_vehicle_id bigint not null,
    level int not null default 1,
    engine_part_id bigint,
    frame_part_id bigint,
    computer_part_id bigint,
    special_part1id bigint,
    special_part2id bigint,
    special_part3id bigint,
    primary key (id)
);

create table vehicle_part (
    id bigserial,
    player_id bigint not null,
    equipped_to bigint,
    type varchar(32) not null,
    level int not null default 1,
    primary key (id)
);

